/** レベル別（累積）: 旧来のXPベース自動昇格用 */
export function getWordsByLevel(level: number): Word[] {
  if (level === 1) return words.filter((w) => w.level === 1);
  if (level === 2) return words.filter((w) => w.level <= 2);
  return words;
}

/** 難易度別（非累積）: ユーザーが選択した難易度専用プール */
export function getWordsByDifficulty(difficulty: 1 | 2 | 3): Word[] {
  return words.filter((w) => w.level === difficulty);
}

export function getRandomWrongOptions(correct: Word, allWords: Word[], count = 3): Word[] {
  // 同じ英語・日本語の単語を除外（重複単語対策）
  const pool = allWords.filter(
    (w) => w.id !== correct.id && w.english !== correct.english && w.japanese !== correct.japanese
  );
  // Fisher-Yates シャッフル（偏りのない真のランダム）
  const shuffled = [...pool];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, count);
}
